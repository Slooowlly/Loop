export function getTeamLogoSrc(teamName) {
  if (typeof teamName !== "string") {
    return null;
  }

  const normalizedName = teamName.trim();
  const canonicalName = TEAM_LOGO_ALIASES[normalizedName] ?? normalizedName;

  return TEAM_LOGO_SRC[canonicalName] ?? null;
}

export default function TeamLogoMark({ teamName, color, size = "md", testId = "standings-team-logo" }) {
  const logoSrc = getTeamLogoSrc(teamName);
  const sizeClass =
    size === "sm"
      ? "h-7 w-[42px]"
      : size === "xs"
        ? "h-6 w-[36px]"
        : "h-9 w-[54px]";
  const fallbackColor = color ?? "#30363d";
  const label = teamName ? `${teamName} logo` : "Logo da equipe";

  if (!logoSrc) {
    return (
      <span
        aria-hidden="true"
        className={[
          "shrink-0 rounded-lg border border-white/10 shadow-[inset_0_1px_0_rgba(255,255,255,0.08)]",
          sizeClass,
        ].join(" ")}
        style={{ backgroundColor: fallbackColor }}
      />
    );
  }

  return (
    <span
      className={[
        "flex aspect-[3/2] shrink-0 items-center justify-center overflow-visible",
        sizeClass,
      ].join(" ")}
    >
      <img
        data-testid={testId}
        src={logoSrc}
        alt={label}
        className="h-full w-full object-contain"
        draggable={false}
      />
    </span>
  );
}
